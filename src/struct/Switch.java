package struct;

public class Switch {
    public static void main(String[] args) {
        String s = "xcw";

        switch(s){
            case "xcw":
                System.out.println("xcw");
                break;
            case "jiangyujun":
                System.out.println("jiangyujun");
                break;
            case "xbb":
                System.out.println("xbb");
                break;
            default:
                System.out.println("jyj");
                break;
        }
    }
}
